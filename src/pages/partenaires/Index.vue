<script>
import moment from "moment";

export default {
  metaInfo: {
    title: "Nos partenaires de confiance",
    meta: [
      {
        name: "description",
        content:
          "Participez à la concrétisation de milliers de projets d’étudiants internationaux",
      },
    ],
  },
  data() {
    return {
      hasnotExcerpt: false,
      loadedPosts: [],
    };
  },
  created() {
    this.loadedPosts.push(...this.$page.articles.belongsTo.edges);
    this.checkExcerpt();
  },
  methods: {
    format_date(value) {
      if (value) {
        return moment(String(value)).format("DD MMMM YYYY");
      }
    },
    async checkExcerpt() {
      for (let post of this.loadedPosts) {
        if (post.node.excerpt === "") {
          this.hasnotExcerpt = true;
        }
      }
    },
  },
  filters: {
    // Filter definitions
    Regular(value) {
      if (typeof window !== "undefined") {
        if (window.innerWidth >= 1024 && window.innerWidth <= 1280) {
          const res = value.split(" ").splice(0, 10).join(" ");
          if (
            res.slice(-1) === "." ||
            res.slice(-1) === "?" ||
            res.slice(-1) === "!"
          ) {
            return res;
          } else {
            return res + "...";
          }
        } else {
          return value;
        }
      }
    },
  },
};
</script>
